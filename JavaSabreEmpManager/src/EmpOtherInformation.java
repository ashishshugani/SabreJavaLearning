public class EmpOtherInformation extends Employee{

    public String type;
    public String dept;

    public void setType(String type){
        this.type=type;
    }

    public String getType(){
        return type;
    }

    public void setDept(String dept){
        this.dept=dept;
    }

    public String getDept(){
        return dept;
    }
}
